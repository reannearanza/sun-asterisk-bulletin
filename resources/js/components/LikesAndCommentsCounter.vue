<script setup lang="ts">
import { defineProps, toRefs } from 'vue';
import { Article } from '../types/Articles';
import { useArticleStore } from '../stores/ArticleStore';

interface LikesAndCommentsCounterProps {
    article: Article
};

const props = defineProps<LikesAndCommentsCounterProps>();
const { article } = toRefs(props);
const articleStore = useArticleStore();

const handleUpvote = () => {
    articleStore.toggleUpvote(article.value.key).then(() => {
        if (article.value.upvotedByUser) {
            article.value.upvotes -= 1;
            article.value.upvotedByUser = false;
        } else {
            article.value.upvotes += 1;
            article.value.upvotedByUser = true;
        }
    });
}
</script>
<template>
  <div class="max-w-6xl mx-auto p-4 mb-4">
    <div class="flex items-center justify-between">
        <span class="flex items-center text-gray-600 mr-4">
            <button
                @click="handleUpvote()"
            >
                <svg xmlns="http://www.w3.org/2000/svg" class="h-6 w-6 mr-1" :fill="article.upvotedByUser ? 'currentColor' : 'none'" viewBox="0 0 24 24" stroke="currentColor">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6.633 10.25c.806 0 1.533-.446 2.031-1.08a9.041 9.041 0 0 1 2.861-2.4c.723-.384 1.35-.956 1.653-1.715a4.498 4.498 0 0 0 .322-1.672V2.75a.75.75 0 0 1 .75-.75 2.25 2.25 0 0 1 2.25 2.25c0 1.152-.26 2.243-.723 3.218-.266.558.107 1.282.725 1.282m0 0h3.126c1.026 0 1.945.694 2.054 1.715.045.422.068.85.068 1.285a11.95 11.95 0 0 1-2.649 7.521c-.388.482-.987.729-1.605.729H13.48c-.483 0-.964-.078-1.423-.23l-3.114-1.04a4.501 4.501 0 0 0-1.423-.23H5.904m10.598-9.75H14.25M5.904 18.5c.083.205.173.405.27.602.197.4-.078.898-.523.898h-.908c-.889 0-1.713-.518-1.972-1.368a12 12 0 0 1-.521-3.507c0-1.553.295-3.036.831-4.398C3.387 9.953 4.167 9.5 5 9.5h1.053c.472 0 .745.556.5.96a8.958 8.958 0 0 0-1.302 4.665c0 1.194.232 2.333.654 3.375Z" />
                </svg>
            </button>
            Upvotes: {{ article.upvotes }}
        </span>
        <span class="flex items-center text-gray-600">
            <svg xmlns="http://www.w3.org/2000/svg" class="h-6 w-6 mr-1" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M8.625 9.75a.375.375 0 1 1-.75 0 .375.375 0 0 1 .75 0Zm0 0H8.25m4.125 0a.375.375 0 1 1-.75 0 .375.375 0 0 1 .75 0Zm0 0H12m4.125 0a.375.375 0 1 1-.75 0 .375.375 0 0 1 .75 0Zm0 0h-.375m-13.5 3.01c0 1.6 1.123 2.994 2.707 3.227 1.087.16 2.185.283 3.293.369V21l4.184-4.183a1.14 1.14 0 0 1 .778-.332 48.294 48.294 0 0 0 5.83-.498c1.585-.233 2.708-1.626 2.708-3.228V6.741c0-1.602-1.123-2.995-2.707-3.228A48.394 48.394 0 0 0 12 3c-2.392 0-4.744.175-7.043.513C3.373 3.746 2.25 5.14 2.25 6.741v6.018Z" />
            </svg>
            Comments: {{ article.comments }}
        </span>
    </div>
</div>
</template>